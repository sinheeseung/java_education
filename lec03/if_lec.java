package lec03;

public class if_lec {

	public static void main(String[] args) {
		
		boolean boolean_a = true;
		
		if(boolean_a) {
			System.out.println("조건식이 참인 경우");
		}
		else {
			System.out.println("조건식이 거짓인 경우");
		}
		
		if(!boolean_a) {
			System.out.println("조건식이 참인 경우");
		}
		else {
			System.out.println("조건식이 거짓인 경우");
		}

	}

}
